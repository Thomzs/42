int	ft_iterative_factorial(int nb)
{
	int result;
	int i;

	i = 2;
	result = nb;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (i <= nb - 1)
		result *= i++;
	return (result);
}
