int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] == s2[i])
	{
		if (s1[i] == '\0')
			return (0);
		i++;
	}
	if (s1[i] == '\0' ^ s2[i] == '\0')
		return (s1[i] == '\0' ? -1 : 1);
	return (s1[i] - s2[i]);
}
