#include "ft_ls.h"

int		ft_strrchr(char *s, int c)
{
	int i;

	i = ft_strlen(s);
	if (s[i] == c)
		return (i);
	while (i > 0)
	{
		i--;
		if (s[i] == c)
			return (i);
	}
	return (0);
}
